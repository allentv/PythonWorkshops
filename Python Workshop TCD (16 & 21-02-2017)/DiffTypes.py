var_str = "Some String"  # String type
var_int = 1  # Integer type
var_float = 1.234  # Floating point type
var_boolean = True  # Other possible value is False
var_none = None  # Null/Empty type


# String operations
print("Actual String			: ", var_str)
print("First Character			: ", var_str[0])
print("3rd Character onwards		: ", var_str[2:])
print("Adding strings			: ", "ABC" + "123")
print("Repeat strings			: ", "H" * 3)
print("Split strings			: ", "ABC 123".split())
print("Last Character			: ", var_str[-1])
print("Reverse a string 		: ", var_str[::-1])
print("\n")


# Integer operations
print("Integer : ", var_int)
print("Float : ", var_float)
print("Boolean : ", var_boolean)
print("Null : ", var_none)
